    private static void makeMazeRecursive(char[][] level, int startX, int startY, int endX, int endY) {

        int select = 0;
        boolean randomRecursion = false;

        if ((endX > 1 && endY > 1)) {

            if ((endX - startX >= 8 && endY - startY >= 8)) {

                //Creates a new random object
                Random rd = new Random();

                //Creates variables for rows and columns
                int Row;
                int Col;

                select = rd.nextInt(2);

                if (select == 1) {
                    randomRecursion = true;
                } else {
                    randomRecursion = false;
                }

                //Initializes Row and Col variables
                if (randomRecursion = false) {
                    Row = (int) ((startY + 1) + (endY - 1)) / 2;
                    Col = (int) ((startX + 1) + (startX - 1)) / 2;
                } else {
                    Row = numBetween((startY + 1), (endY - 1));
                    Col = numBetween((startX + 1), (endX - 1));
                }

                //Creates row walls within outer barriers
                for (int i = startX; i <= endX; i++) {
                    level[Row][i] = ICON_WALL;
                }

                //Creates column walls within outer barriers
                for (int j = startY; j <= endY; j++) {
                    level[j][Col] = ICON_WALL;
                }

                //creates random holes in row walls
                if (Row == 1) {
                    int lowerLeft = numBetween(startX, Col - 1);
                    int upperLeft = numBetween(Col + 1, endX);

                    level[Row][lowerLeft] = ICON_BLANK;
                    level[Row][upperLeft] = ICON_BLANK;
                }

                        //Creates random holes in column walls
                        if (Col == 1) {
                            int lowerRight = numBetween(startY, Row - 1);
                            int upperRight = numBetween(Row + 1, endY);

                            level[Col][lowerRight] = ICON_BLANK;
                            level[Col][upperRight] = ICON_BLANK;
                        }

                        level[Row][Col] = ICON_BLANK;

                    //Recursive calls
                    makeMazeRecursive(level, 1, 1, (Col - 1), (Row + 1));
                    makeMazeRecursive(level, (Col + 1), 1, endX, (Row - 1));
                    makeMazeRecursive(level, startX, (Row + 1), (Col - 1), endY);
                    makeMazeRecursive(level, (Col - 1), (Row + 1), endX, endY);

                }

            }

        }

