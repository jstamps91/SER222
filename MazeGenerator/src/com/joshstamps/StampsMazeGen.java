    private static void drawLevel(char[][] level) {
        int y, x;

        for (y = 0; y < LEVEL_HEIGHT; y++) {
            for (x = 0; x < LEVEL_WIDTH; x++)
                System.out.print(level[y][x]);
            System.out.println();
        }
    }

    private static int numBetween(int lower, int upper) {

        //Upper limit - Lower Limit + 1 + lower Limit
        Random rd = new Random();

        int result = rd.nextInt((upper - lower) + 1) + lower;

        return result;

    }


